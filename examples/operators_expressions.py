# arithmetic operators
x = 5
y = 2

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x**y)  # exponent operator
print(x % y)  # modulo operator
print(x // y)  # floor division operator

# comparison operators
print(x == y)
print(x != y)
print(x > y)
print(x < y)
print(x >= y)
print(x <= y)

# logical operators
a = True
b = False

print(a and b)
print(a or b)
print(not a)
print(not b)

# assignment operators
x = 5
x += 5
print(x)
x -= 5
print(x)
x *= 5
print(x)
x /= 5
print(x)

# expressions are calculated in mathematical order
x = 5 + 5 * 5  # what will this print?
print(x)
