# while loop

i = 0

while i < 10:
    print(i)
    i += 1

# for loop
# this loop is used to iterate over a sequence
# range() function is used to generate a sequence of numbers
# range() function takes 3 arguments
# range(start, stop, step)
# start is the starting number of the sequence
# stop is the number after which the sequence stops
# step is the difference between two consecutive numbers in the sequence

for i in range(5):
    print(i)

for i in range(1, 5):
    print(i)

for i in range(1, 5, 2):
    print(i)
