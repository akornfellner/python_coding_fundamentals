# if is used to check a condition
x = 5  # try changing this value

if x > 3:
    print("x is greater than 3")

# if-else is used to check a condition and provide an alternative
x = 1  # try changing this value

if x > 3:
    print("x is greater than 3")
else:
    print("x is not greater than 3")

# if-elif-else is used to check multiple conditions
x = 1  # try changing this value

if x > 3:
    print("x is greater than 3")
elif x < 3:
    print("x is less than 3")
else:
    print("x is equal to 3")

# use can use more statements using blocks
# in python blocks are defined by indentation

x = 1  # try changing this value

if x > 3:
    print("x is greater than 3")
    print("x is not less than 3")
    print("x is not equal to 3")
else:
    print("x is less than 3")
    print("x is not greater than 3")
    print("x is not equal to 3")
