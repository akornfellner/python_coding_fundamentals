# variable x is an integer
x = 5

# variable y is a float
y = 5.0

# variables can be assigned using expressions
s = x + y

# variable z is a string
z = "Hello world"

# variable a is a boolean
a = True

print(x, y, s, z, a)

# variables can be reassigned
x = 10

print(x)

# varibles can be cast to other types

# cast x to a string
x = str(x)

# cast "5" to an integer
x = "5"

print(2 * x)

y = int(x)

print(2 * y)
